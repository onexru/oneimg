<script setup>
import { ref, onMounted, computed } from 'vue'
import NavBar from '@/components/NavBar.vue'

// ÂìçÂ∫îÂºèÊï∞ÊçÆ
const images = ref([])
const loading = ref(false)
const searchQuery = ref('')
const sortBy = ref('created_at')
const sortOrder = ref('desc')
const viewMode = ref('grid')
const currentPage = ref(1)
const totalPages = ref(1)
const pageSize = ref(20)

// È¢ÑËßàÊ®°ÊÄÅÊ°Ü
const previewModal = ref({
    show: false,
    image: null
})

// ÈÄöÁü•Ê∂àÊÅØ
const notification = ref({
    show: false,
    message: '',
    type: 'success'
})

// ËÆ°ÁÆóÂ±ûÊÄß
const visiblePages = computed(() => {
    const pages = []
    const start = Math.max(1, currentPage.value - 2)
    const end = Math.min(totalPages.value, currentPage.value + 2)
    
    for (let i = start; i <= end; i++) {
        pages.push(i)
    }
    
    return pages
})

// Âä†ËΩΩÂõæÁâáÂàóË°®
const loadImages = async () => {
    loading.value = true
    
    try {
        const params = new URLSearchParams({
            page: currentPage.value,
            limit: pageSize.value,
            sort_by: sortBy.value,
            sort_order: sortOrder.value
        })
        
        if (searchQuery.value) {
            params.append('search', searchQuery.value)
        }
        
        const response = await fetch(`/api/images?${params}`, {
            headers: {
                'Authorization': `Bearer ${localStorage.getItem('authToken')}`
            }
        })
        
        if (response.ok) {
            const result = await response.json()
            images.value = result.data.images || []
            totalPages.value = result.data.total_pages || 1
        } else {
            throw new Error('Âä†ËΩΩÂõæÁâáÂ§±Ë¥•')
        }
    } catch (error) {
        console.error('Âä†ËΩΩÂõæÁâáÈîôËØØ:', error)
        showNotification('Âä†ËΩΩÂõæÁâáÂ§±Ë¥•: ' + error.message, 'error')
    } finally {
        loading.value = false
    }
}

// ÊêúÁ¥¢Â§ÑÁêÜ
let searchTimeout = null
const handleSearch = () => {
    clearTimeout(searchTimeout)
    searchTimeout = setTimeout(() => {
        currentPage.value = 1
        loadImages()
    }, 500)
}

// ÂàÜÈ°µÂ§ÑÁêÜ
const changePage = (page) => {
    if (page >= 1 && page <= totalPages.value) {
        currentPage.value = page
        loadImages()
        // ÊªöÂä®Âà∞È°∂ÈÉ®
        window.scrollTo({ top: 0, behavior: 'smooth' })
    }
}

// ÂõæÁâáÈ¢ÑËßà
const openPreview = (image) => {
    previewModal.value = {
        show: true,
        image: image
    }
}

const closePreview = () => {
    previewModal.value.show = false
}

// Â§çÂà∂ÈìæÊé•
const copyUrl = async (url) => {
    try {
        const fullUrl = window.location.origin + url
        await navigator.clipboard.writeText(fullUrl)
        showNotification('ÈìæÊé•Â∑≤Â§çÂà∂Âà∞Ââ™Ë¥¥Êùø', 'success')
    } catch (error) {
        // ÈôçÁ∫ßÊñπÊ°à
        const textArea = document.createElement('textarea')
        textArea.value = window.location.origin + url
        document.body.appendChild(textArea)
        textArea.select()
        document.execCommand('copy')
        document.body.removeChild(textArea)
        showNotification('ÈìæÊé•Â∑≤Â§çÂà∂Âà∞Ââ™Ë¥¥Êùø', 'success')
    }
}

// ‰∏ãËΩΩÂõæÁâá
const downloadImage = (image) => {
    const link = document.createElement('a')
    link.href = image.url
    link.download = image.filename
    document.body.appendChild(link)
    link.click()
    document.body.removeChild(link)
}

// Âà†Èô§ÂõæÁâá
const deleteImage = async (image) => {
    if (!confirm(`Á°ÆÂÆöË¶ÅÂà†Èô§ÂõæÁâá "${image.filename}" ÂêóÔºü`)) {
        return
    }
    
    try {
        const response = await fetch(`/api/images/${image.id}`, {
            method: 'DELETE',
            headers: {
                'Authorization': `Bearer ${localStorage.getItem('authToken')}`
            }
        })
        
        if (response.ok) {
            showNotification('ÂõæÁâáÂà†Èô§ÊàêÂäü', 'success')
            // ÂÖ≥Èó≠È¢ÑËßàÊ®°ÊÄÅÊ°ÜÔºàÂ¶ÇÊûúÊâìÂºÄÁöÑËØùÔºâ
            if (previewModal.value.show && previewModal.value.image.id === image.id) {
                closePreview()
            }
            // ÈáçÊñ∞Âä†ËΩΩÂõæÁâáÂàóË°®
            loadImages()
        } else {
            const result = await response.json()
            throw new Error(result.message || 'Âà†Èô§Â§±Ë¥•')
        }
    } catch (error) {
        console.error('Âà†Èô§ÂõæÁâáÈîôËØØ:', error)
        showNotification('Âà†Èô§ÂõæÁâáÂ§±Ë¥•: ' + error.message, 'error')
    }
}

